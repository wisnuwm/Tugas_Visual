    private void datatable(){
        Object[] Baris = {"No Transaksi", "Tanggal","Nama Sepatu","Warna","Harga","Jumlah Beli","Sub Total","Total","Bayar","Kembalian"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_transaksi.setModel(tabmode);
        String sql1 = "SELECT * FROM `transpenjualan`";
        try{
            java.sql.Statement stat = con.createStatement();
            ResultSet hasil = stat.executeQuery(sql1);
            while(hasil.next()){
                String a = hasil.getString("no_transaksi");
                String b = hasil.getString("tanggal");
                String c = hasil.getString("nama_sepatu");
                String d = hasil.getString("warna");
                String e = hasil.getString("harga");
                String f = hasil.getString("jumlah_beli");
                String g = hasil.getString("sub_total");
                String h = hasil.getString("total");
                String i = hasil.getString("bayar");
                String j = hasil.getString("kembalian");
                
                
                String[] data ={a,b,c,d,e,f,g,h,i,j};
                tabmode.addRow(data);
            }
            
            
        } catch (Exception e) { 
            JOptionPane.showMessageDialog(null,e);
    }
    }

