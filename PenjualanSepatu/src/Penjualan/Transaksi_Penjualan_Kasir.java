    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO `transpenjualan`( `tanggal`, `nama_sepatu`, `warna`, `harga`, `jumlah_beli`, `sub_total`, `total`, `bayar`, `kembalian`) VALUES (?,?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = con.prepareStatement(sql);           
            stat.setString(1, tgl);
            stat.setString(2, nama_sepatu.getText());
            stat.setString(3, warna.getText());
            stat.setString(4, harga.getText());
            stat.setString(5, jumlah_beli.getText());
            stat.setString(6, sub_total.getText());
            stat.setString(7, total.getText());
            stat.setString(8, bayar.getText());
            stat.setString(9, kembalian.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
            kosongkan();
            no_trans.requestFocus();
            datatable();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan " +e);
        }
      

    }//GEN-LAST:event_btnsimpanActionPerformed

    private void cari_sepatuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cari_sepatuActionPerformed
        // TODO add your handling code here:
        String sql = "select * from inputbrg where kode_sepatu ='"+kode_sepatu.getText()+"'";
        try {
            java.sql.Statement stat= con.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String kd = hasil.getString("nama_sepatu");
                String nm = hasil.getString("warna");
                String hrg = hasil.getString("harga");
                
                nama_sepatu.setText(kd);
                warna.setText(nm);
                harga.setText(hrg);
                
                nama_sepatu.setEnabled(false);
                warna.setEnabled(false);
                harga.setEnabled(false);
            }
        } catch (Exception e){
            
        }

    }//GEN-LAST:event_cari_sepatuActionPerformed

    private void bayarCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_bayarCaretUpdate
        // TODO add your handling code here:
        double a;
        double b;
        double c;
        a = Double.parseDouble(bayar.getText());
        b = Double.parseDouble(total.getText());
        c = a - b;
        kembalian.setText(String.valueOf(c));
        kembalian.setEnabled(false);    

    }//GEN-LAST:event_bayarCaretUpdate

    private void jumlah_beliCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jumlah_beliCaretUpdate
        // TODO add your handling code here:
        double d,e,f;
        d = Double.parseDouble(harga.getText());
        e = Double.parseDouble(jumlah_beli.getText());
        f = d * e;
        sub_total.setText(String.format("%.2f", f));
        sub_total.setEnabled(false);
        total.setText(String.format("%.2f", f));

    }//GEN-LAST:event_jumlah_beliCaretUpdate

