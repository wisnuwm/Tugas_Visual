    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO `transpembelian`( `tanggal`,`kodesupplier`, `namasupplier`, `kodebarang`, `namabarang`, `hargasatuan`, `qty`, `total`) VALUES (?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = con.prepareStatement(sql);           
            stat.setString(1, tgl);
            stat.setString(2, kodesupplier.getText());
            stat.setString(3, namasupplier.getText());
            stat.setString(4, kodebarang.getText());
            stat.setString(5, namabarang.getText());
            stat.setString(6, hargasatuan.getText());
            stat.setString(7, qty.getText());
            stat.setString(8, total.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
            kosongkan();
            notrans.requestFocus();
            datatable();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan " +e);
        }
    }//GEN-LAST:event_saveActionPerformed

