    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "Update transpembelian set tanggal=?,kodesupplier=?,namasupplier=?,kodebarang=?,namabarang=?,hargasatuan=?,qty=?,total=? where notrans=?";
            PreparedStatement stat = con.prepareStatement (sql);
            stat.setString(1, tanggal.getDateFormatString());
            stat.setString(2, kodesupplier.getText());
            stat.setString(3, namasupplier.getText());
            stat.setString(4, kodebarang.getText());
            stat.setString(5, namabarang.getText());
            stat.setString(6, hargasatuan.getText());
            stat.setString(7, qty.getText());
            stat.setString(8, total.getText());
            stat.setString(9, notrans.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Diubah");
            kosongkan();
            notrans.requestFocus();
            datatable();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah" +e);
        }
    }//GEN-LAST:event_editActionPerformed

