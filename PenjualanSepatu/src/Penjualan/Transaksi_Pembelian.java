    private void datatable(){
        Object[] Baris = {"No Transaksi", "Tanggal","Kode Supplier","Nama Supplier","Kode Barang","Nama Barang","Harga Satuan","Qty","Total"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_transaksi.setModel(tabmode);
        String sql1 = "SELECT * FROM `transpembelian`";
        try{
            java.sql.Statement stat = con.createStatement();
            ResultSet hasil = stat.executeQuery(sql1);
            while(hasil.next()){
                String a = hasil.getString("notrans");
                String b = hasil.getString("tanggal");
                String c = hasil.getString("kodesupplier");
                String d = hasil.getString("namasupplier");
                String e = hasil.getString("kodebarang");
                String f = hasil.getString("namabarang");
                String g = hasil.getString("hargasatuan");
                String h = hasil.getString("qty");
                String i = hasil.getString("total");
                
                
                String[] data ={a,b,c,d,e,f,g,h,i};
                tabmode.addRow(data);
            }
            
            
        } catch (Exception e) { 
            JOptionPane.showMessageDialog(null,e);
    }
    }

